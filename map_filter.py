# Syntax: list(map(fn, lst))


# filter function: filters the values of list, includes only those numbers which are returned true in the condition
# Syntax: list(filter(fn, lst))

# 'reduce' function: to reduce the items in the list to a single number cummilatively. Shortcut to apply same operation on the list items. 
# Syntax: 
